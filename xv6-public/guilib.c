// ---- Utility functions ----

void gui_strcpy(char *dst, char *src) {
  while((*dst++ = *src++) != 0);
}

int gui_strlen(char *s) {
  int n = 0;
  while(s[n]) n++;
  return n;
}

void gui_strcat(char *dst, char *src) {
  while(*dst) dst++;
  while((*dst++ = *src++) != 0);
}

void gui_itoa(int n, char *buf) {
  char tmp[16];
  int i = 0, neg = 0;
  if(n < 0){ neg = 1; n = -n; }
  if(n == 0){ buf[0] = '0'; buf[1] = 0; return; }
  while(n > 0){ tmp[i++] = '0' + (n % 10); n /= 10; }
  int j = 0;
  if(neg) buf[j++] = '-';
  while(i > 0) buf[j++] = tmp[--i];
  buf[j] = 0;
}

int point_in_rect(int px, int py, int rx, int ry, int rw, int rh) {
  return px >= rx && px < rx + rw && py >= ry && py < ry + rh;
}

// ---- Drawing primitives ----

void fb_clear(struct framebuf *fb, uint color) {
  int total = fb->width * fb->height;
  for(int i = 0; i < total; i++)
    fb->pixels[i] = color;
}

void fb_pixel(struct framebuf *fb, int x, int y, uint color) {
  if(x >= 0 && x < fb->width && y >= 0 && y < fb->height)
    fb->pixels[y * fb->width + x] = color;
}

void fb_fill_rect(struct framebuf *fb, int x, int y, int w, int h, uint color) {
  int x0 = x < 0 ? 0 : x;
  int y0 = y < 0 ? 0 : y;
  int x1 = x + w > fb->width ? fb->width : x + w;
  int y1 = y + h > fb->height ? fb->height : y + h;
  int count = x1 - x0;
  if(count <= 0) return;
  for(int j = y0; j < y1; j++){
    uint *row = &fb->pixels[j * fb->width + x0];
    for(int i = 0; i < count; i++)
      row[i] = color;
  }
}

void fb_rect(struct framebuf *fb, int x, int y, int w, int h, uint color) {
  for(int i = x; i < x + w; i++){
    fb_pixel(fb, i, y, color);
    fb_pixel(fb, i, y + h - 1, color);
  }
  for(int j = y; j < y + h; j++){
    fb_pixel(fb, x, j, color);
    fb_pixel(fb, x + w - 1, j, color);
  }
}

void fb_line(struct framebuf *fb, int x0, int y0, int x1, int y1, uint color) {
  int dx = x1 - x0;
  int dy = y1 - y0;
  int sx = dx > 0 ? 1 : -1;
  int sy = dy > 0 ? 1 : -1;
  dx = dx < 0 ? -dx : dx;
  dy = dy < 0 ? -dy : dy;
  int err = dx - dy;
  while(1){
    fb_pixel(fb, x0, y0, color);
    if(x0 == x1 && y0 == y1) break;
    int e2 = 2 * err;
    if(e2 > -dy){ err -= dy; x0 += sx; }
    if(e2 < dx){ err += dx; y0 += sy; }
  }
}

void fb_circle(struct framebuf *fb, int cx, int cy, int r, uint color) {
  int x = r, y = 0, err = 0;
  while(x >= y){
    fb_pixel(fb, cx+x, cy+y, color); fb_pixel(fb, cx+y, cy+x, color);
    fb_pixel(fb, cx-y, cy+x, color); fb_pixel(fb, cx-x, cy+y, color);
    fb_pixel(fb, cx-x, cy-y, color); fb_pixel(fb, cx-y, cy-x, color);
    fb_pixel(fb, cx+y, cy-x, color); fb_pixel(fb, cx+x, cy-y, color);
    if(err <= 0){ y++; err += 2*y + 1; }
    if(err > 0){ x--; err -= 2*x + 1; }
  }
}

void fb_fill_circle(struct framebuf *fb, int cx, int cy, int r, uint color) {
  for(int y = -r; y <= r; y++)
    for(int x = -r; x <= r; x++)
      if(x*x + y*y <= r*r)
        fb_pixel(fb, cx+x, cy+y, color);
}

void fb_gradient_v(struct framebuf *fb, int x, int y, int w, int h,
                   uint c1, uint c2) {
  for(int j = 0; j < h; j++){
    int r1 = (c1 >> 16) & 0xFF, g1 = (c1 >> 8) & 0xFF, b1 = c1 & 0xFF;
    int r2 = (c2 >> 16) & 0xFF, g2 = (c2 >> 8) & 0xFF, b2 = c2 & 0xFF;
    int r = r1 + (r2 - r1) * j / h;
    int g = g1 + (g2 - g1) * j / h;
    int b = b1 + (b2 - b1) * j / h;
    uint color = RGB(r, g, b);
    for(int i = x; i < x + w && i < fb->width; i++)
      if(j + y >= 0 && j + y < fb->height && i >= 0)
        fb->pixels[(j + y) * fb->width + i] = color;
  }
}

// ---- Text rendering ----

void fb_char(struct framebuf *fb, int x, int y, char c, uint fg, uint bg) {
  if(c < 32 || c > 126) c = '?';
  uchar *glyph = font8x16[c - 32];
  for(int row = 0; row < FONT_H; row++){
    uchar bits = glyph[row];
    for(int col = 0; col < FONT_W; col++){
      uint color = (bits & (0x80 >> col)) ? fg : bg;
      fb_pixel(fb, x + col, y + row, color);
    }
  }
}

void fb_text(struct framebuf *fb, int x, int y, char *s, uint fg, uint bg) {
  while(*s){
    if(*s == '\n'){
      y += FONT_H;
      x = 0; // crude, caller should manage x
    } else {
      fb_char(fb, x, y, *s, fg, bg);
      x += FONT_W;
    }
    s++;
  }
}

void fb_text_nobg(struct framebuf *fb, int x, int y, char *s, uint fg) {
  while(*s){
    if(*s >= 32 && *s <= 126){
      uchar *glyph = font8x16[*s - 32];
      for(int row = 0; row < FONT_H; row++){
        uchar bits = glyph[row];
        for(int col = 0; col < FONT_W; col++){
          if(bits & (0x80 >> col))
            fb_pixel(fb, x + col, y + row, fg);
        }
      }
      x += FONT_W;
    }
    s++;
  }
}

int fb_text_width(char *s) {
  return gui_strlen(s) * FONT_W;
}

