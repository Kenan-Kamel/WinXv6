// ---- Mouse cursor ----

void draw_cursor(struct framebuf *fb, int mx, int my) {
  for(int row = 0; row < 19; row++){
    for(int col = 0; col < 12; col++){
      int byte_idx = col / 8;
      int bit_idx = 7 - (col % 8);
      if(cursor_mask[row * 2 + byte_idx] & (1 << bit_idx)){
        uint color;
        if(cursor_data[row * 2 + byte_idx] & (1 << bit_idx))
          color = COL_TEXT_WHITE;
        else
          color = COL_TEXT_BLACK;
        fb_pixel(fb, mx + col, my + row, color);
      }
    }
  }
}
